Zahlen sind Felder, die ausschließlich numerische Eingaben erlauben.
Sie eignen sich besonders für Berechnungen wie Prozentrechnungen und können bei Bedarf als ausgeschriebene Zahlen im Dokument dargestellt werden.
<br><br>
<b>Achten Sie darauf, dass im Select „Vorlage in Bearbeitung“ die richtige Vorlage ausgewählt ist.</b>