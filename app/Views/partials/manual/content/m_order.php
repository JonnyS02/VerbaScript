Die Anordnung ist ein Interface, dass genutzt wird, um Formulare von Vorlagen zu designen.
<b>Achten Sie darauf, dass im Select „Vorlage in Bearbeitung“ die richtige Vorlage ausgewählt ist.</b>
<br><br>
Um eine Vorschau der Vorlage zu sehen, klicken Sie auf „Live Vorschau“ am unteren Rand der Seite.
Achten Sie darauf, dass der Button „automatisch speichern“ aktiviert ist, damit die Änderungen sofort übernommen werden.
Es wird empfohlen, die Livevorschau und das Interface in getrennten Tabs gleichzeitig zu öffnen, um die Änderungen direkt zu sehen.
<br><br>
In der Anordnungstabelle sind alle mit der Vorlage verbundenen Elemente aufgelistet.
Die einzelnen Spalten geben verschieden Optionen zur Bearbeitung der jeweiligen Elemente.
Fahren Sie mit dem Mauszeiger über die Spaltenüberschriften, um eine Erklärung zu erhalten.
Um die Position der Elemente zu ändern, klicken Sie in die Nähe des Namen des Elements und ziehen sie es an die gewünschte Position.
