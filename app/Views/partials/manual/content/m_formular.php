Wählen Sie ein Formular aus, um ein Dokument aus einer bestimmten Vorlage zu erstellen.
Sie können das Formular ggf. auch durch eine Spracheingabe ausfüllen (prüfen Sie jedoch die Richtigkeit der Daten).
<br><br>
Sie können die Spracheingabe mit dem Befehl „Los“ absenden oder mit dem Befehl „Stopp“ anhalten. Der Stopp-Befehl ist bei lokalen Spracheingaben nicht verfügbar.
Nach ca. 3 Sekunden Stille wird die Spracheingabe automatisch beendet.
<br><br>
Dokumente können nur von Formularen generiert werden, bei denen alle verpflichtenden Felder ausgefüllt wurden.
Diese Formulare werden in der Tabelle mit einem Häkchen nach dem Namen gekennzeichnet.