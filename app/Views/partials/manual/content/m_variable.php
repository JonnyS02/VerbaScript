Variablen sind die grundlegenden Felder in Formularen und dienen der Eingabe von einfachem Text ohne spezielle Verarbeitung.
Sie können bei Bedarf in spezielle Felder wie E-Mail- oder Datumsfelder umgewandelt werden.
<br><br>
<b>Achten Sie darauf, dass im Select „Vorlage in Bearbeitung“ die richtige Vorlage ausgewählt ist.</b>
