Selects sind Dropdown-Felder, die es ermöglichen, vorab definierte Antwortmöglichkeiten auszuwählen.
Optional kann auch eine individuelle Eingabe aktiviert werden, um zusätzliche Flexibilität zu gewährleisten.
<br><br>
<b>Achten Sie darauf, dass im Select „Vorlage in Bearbeitung“ die richtige Vorlage ausgewählt ist.</b>