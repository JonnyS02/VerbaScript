Gruppen sind Hilfselemente, die zur besseren Strukturierung der Formulare dienen.
Sie erscheinen nicht direkt als Felder im Formular, ermöglichen jedoch das Gruppieren von Elementen,
wodurch diese in Tabellen leichter auffindbar werden. Zusätzlich können Gruppen als Überschriften im Formular angezeigt werden,
um die Übersichtlichkeit zu erhöhen.
<br><br>
<b>Achten Sie darauf, dass im Select „Vorlage in Bearbeitung“ die richtige Vorlage ausgewählt ist.</b>